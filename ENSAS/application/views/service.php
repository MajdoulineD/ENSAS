	    						<a href="#"><h3>Enregistrement des absences /Avertissement</h3></a><br>
	    						<div>
	    						Les absences et les retards des élèves seront enregistrés à chaque période de cours par le
professeur. Durant le deuxième jour d’absence au plus tard, les parents ou l’élève majeur doivent
présenter une excuse écrite pour l’élève concerné, et après plus de deux jours d’absence, ils
doivent présenter un certificat médical.<br> L'École envoie régulièrement aux parents une liste des
absences qui n’ont été pas motivées. Les absences non autorisées seront clairement identifiées
et feront l'objet d'un traitement sévère. <br>Si le nombre de périodes pendant lesquelles un élève est
absent à un cours dépasse le seuil des 10% le Directeur en avertira le chef de famille ou l'élève
majeur et le rendra attentif aux risques encourus.<br>
<b>Si au cours de l'année scolaire, le nombre de périodes pendant lesquelles un élève est absent à
un cours est tel que le risque existe de ne pouvoir établir une note A, le Directeur avertira le chef
de famille ou l'élève majeur des conséquences: <br><br>
 $ En cas d'absence non justifiée pendant plus de quinze jours consécutifs, l'élève est
considéré comme ayant quitté l'école. 
<br>
$ Si des absences répétées non justifiées d’une ou plusieurs périodes continuent à se
produire après l'avertissement du Directeur, le Conseil de discipline peut prononcer
l'exclusion de l'élève. <br>
$ En cas de décès d'un parent jusqu'au deuxième degré, la durée de l'absence autorisée
peut être prolongée. <br>
$ Au cas où un élève est empêché de fréquenter l’Ecole pour raison de santé, les parents
informeront le Directeur de la raison de l'absence, par écrit et au plus tard le deuxième jour de
l'absence.<br>
$ Toute absence non motivée impliquera le retrait de la carte de sortie. 


</b>
