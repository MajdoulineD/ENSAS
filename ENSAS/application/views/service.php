	    						L'école est un lieu destiné à l’enseignement et à l'éducation des élèves; formation et éducation
doivent s'entendre dans un sens large, intégrant également l'apprentissage des élèves à la vie
sociale, à ses valeurs, aux droits et aux devoirs que cela impose. Les règles de vie en commun
doivent donc être définies afin d'assurer le bon fonctionnement quotidien de l'école. <br><br>
<p>Ce règlement est basé sur le respect mutuel et la solidarité entre tous les membres de la
communauté scolaire. Il sous-entend le respect des valeurs liées à la personne, sans
distinction de fonction, d’âge, de couleur de peau, de sexe, de nationalité, de croyance ou
convictions philosophiques. </p><br>

	<font color="#286828"><i><b>Citons ce fameux Réglement :</b></i></font></div><br>
<b>$ Le respect des heures de début et de fin de cours s'impose pour toute la communauté scolaire.
Tout retard d'un élève sera signalé par le professeur au conseiller d'éducation. L'élève qui arrive
en retard doit se rendre en classe et présenter une justification au professeur.</b> <br><br>
<b>$ Les devoirs à domicile doivent être faits pour la date convenue afin de permettre une correction
efficace par l'enseignant.</b><br><br>
<b>$ Les élèves qui quittent l'enceinte de l'école pendant la journée scolaire (heures libres ou heures
libérées suite à l'absence d'un ou de plusieurs professeurs) ne sont pas couverts par l'assurance
scolaire. </b><br>
